function validateEntry(entry) {
  if (entry.seoTitle.length > 60) {
    throw new Error(`seoTitle > 60 (${entry.seoTitle.length}): [${entry.pageSlug}] ${entry.seoTitle}`);
  }
  if (entry.seoDescription.length > 160) {
    throw new Error(
      `seoDescription > 160 (${entry.seoDescription.length}): [${entry.pageSlug}] ${entry.seoDescription}`
    );
  }
}

async function seedPageSeo() {
  PAGE_SEO.forEach(validateEntry);

  const pool = new Pool({
    user: process.env.DB_USER || 'postgres',
    password: process.env.DB_PASSWORD,
    host: process.env.DB_HOST || 'localhost',
    port: Number(process.env.DB_PORT || 5432),
    database: process.env.DB_NAME || 'stonerose_db',
  });

  try {
    console.log(`🔄 SEO: upsert ${PAGE_SEO.length} страниц (${CITY}, ${BRAND})...`);

    let inserted = 0;
    let updated = 0;
    let skipped = 0;
    const forceUpdate = process.argv.includes('--force');

    for (const row of PAGE_SEO) {
      const isIndexed = row.isIndexed !== false;
      const existing = await pool.query('SELECT id FROM page_seo WHERE page_slug = $1', [row.pageSlug]);

      if (existing.rows.length === 0) {
        await pool.query(
          `INSERT INTO page_seo (
            page_slug, page_title, seo_title, seo_description, seo_keywords, is_indexed, updated_at
          ) VALUES ($1, $2, $3, $4, $5, $6, NOW())`,
          [row.pageSlug, row.pageTitle, row.seoTitle, row.seoDescription, row.seoKeywords, isIndexed]
        );
        inserted++;
        console.log(`  + ${row.pageSlug}`);
      } else if (forceUpdate) {
        await pool.query(
          `UPDATE page_seo SET
            page_title = $2,
            seo_title = $3,
            seo_description = $4,
            seo_keywords = $5,
            is_indexed = $6,
            updated_at = NOW()
          WHERE page_slug = $1`,
          [row.pageSlug, row.pageTitle, row.seoTitle, row.seoDescription, row.seoKeywords, isIndexed]
        );
        updated++;
        console.log(`  ~ ${row.pageSlug}`);
      } else {
        skipped++;
        console.log(`  = ${row.pageSlug} (без изменений)`);
      }
    }

    // Устаревший slug после переименования каталога
    await pool.query(`DELETE FROM page_seo WHERE page_slug = 'monuments'`);

    console.log(`✅ Готово: создано ${inserted}, обновлено ${updated}, пропущено ${skipped}`);
  } catch (error) {
    console.error('❌ Ошибка seed SEO:', error);
    process.exitCode = 1;
  } finally {
    await pool.end();
  }
}

seedPageSeo();
